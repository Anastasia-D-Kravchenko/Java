public class Sep_Calc{
   public static void main(String[] args) {
       int var3 = 4;
       System.out.println(var3);
       var3 += 3;
       System.out.println(var3);
       var3 *= 2;
       System.out.println(var3);
       var3 /= 4;
       System.out.println(var3);
       var3 -= 1;
       System.out.println(var3);
       var3 %= 3;
       System.out.println(var3);
   }
}