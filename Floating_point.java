public class Floating_point{
   public static void main(String[] args) {
       System.out.println(4 / 3); // Integer division (1)
       System.out.println(4 / 3.0); // Floating-point division (1.3333333333333333)
       System.out.println(4.0 / 3); // Floating-point division (1.3333333333333333)
       System.out.println(4.0 / 3.0); // Floating-point division (1.3333333333333333)
   }
}