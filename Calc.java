public class Calc{
   public static void main(String[] args) {
       int var1 = 3;
       int var2 = 12;
       System.out.println(3 + 12); // Direct calculation
       System.out.println(var1 + var2); // Using variables

       System.out.println(var1 * var2); // Multiplication
       System.out.println(var1 / var2); // Division
       System.out.println(var1 - var2); // Subtraction
       System.out.println(var1 % var2); // Modulo
   }
}